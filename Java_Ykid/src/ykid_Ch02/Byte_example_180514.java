package ykid_Ch02;

public class Byte_example_180514 {
	public static void main(String[] args) {
		/*
		 * var 변수는 모두 byte 타입으로 선언
		 * var1에 -128대입
		 * var2에 -30대입
		 * var3에 0대입
		 * var4에 30대입
		 * var5에 127대입
		 * var6에 128대입
		 * 그리고 각각 변수 값을 출력하세요.
		 */
		byte var1 = -128;
		byte var2 = -30;
		byte var3 = -0;
		byte var4 = 30;
		byte var5 = 127;
		//byte var6 = 128;
		System.out.println(var1);
		System.out.println(var2);
		System.out.println(var3);
		System.out.println(var4);
		System.out.println(var5);
		//System.out.println(var6);
		
	}

}
