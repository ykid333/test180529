package ykid_Ch04;

public class Ch4_WhilePrintfrom1to10Example {
	
	public static void main(String[] args) {
		int sum=0;
		int i = 1;
		while(i<=10) {			
			sum=sum+i;
			i++;		
		}
		System.out.println(sum);
		System.out.println("!!!!!!!!!!!!!");
		
		
		
		
		
		
		
	}
	
}
